        // CODE FOR CHANGE PASSWORD
        try
        {
          String opswd=jPasswordField1.getText(); 
          String npswd=jPasswordField2.getText(); 
          String cpswd=jPasswordField3.getText(); 
          DBConnect db=new DBConnect();
          db.pstmt=db.con.prepareStatement("select * from login where user=? and password=?");
          db.pstmt.setString(1,user);
          db.pstmt.setString(2,opswd);
          db.rst=db.pstmt.executeQuery();
          if(db.rst.next())
          {
              if(npswd.equals(cpswd))
              {
                  db.pstmt=db.con.prepareStatement("update login set password=? where user=?");
                  db.pstmt.setString(2,user);
                  db.pstmt.setString(1,cpswd);
                  int i=db.pstmt.executeUpdate();
                  if(i>0)
                  {
                    JOptionPane.showMessageDialog(this,"PASSWORD UPDATED SUCCESSFULLY");  
                    ChangePassword.setVisible(false);
                  }
              }
              else{
                  JOptionPane.showMessageDialog(this,"NEW AND CONFIRM PASSWORD DOES NOT MATCHED");  
              }
          }
          else{
              JOptionPane.showMessageDialog(this,"OLD PASSWORD DOES NOT MATCHED");  
          }
          
        }
